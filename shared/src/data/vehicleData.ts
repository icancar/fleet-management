export const getVehicleBrands = (): string[] => {
  return VEHICLE_BRANDS.map(brand => brand.name);
};

export const getVehicleModels = (brandName: string): string[] => {
  const brand = VEHICLE_BRANDS.find(b => b.name === brandName);
  return brand ? brand.models : [];
};

export const isValidVehicleBrand = (brandName: string): boolean => {
  return VEHICLE_BRANDS.some(brand => brand.name === brandName);
};

export const isValidVehicleModel = (brandName: string, modelName: string): boolean => {
  const brand = VEHICLE_BRANDS.find(b => b.name === brandName);
  return brand ? brand.models.includes(modelName) : false;
};